## Typecasting: str(), int(), float(), bool()

name = "Faga Imam"
age = 19
gpa = 3.75
is_student = True

print(type(name))
print(type(age))
print(type(gpa))
print(type(is_student))

# float to int
gpa2 = int(gpa)
print(gpa2)

# int to float
age2 = float(age)
print(age2)

# int to str
age3 = str(age)
print(age3)
print(type(age3))