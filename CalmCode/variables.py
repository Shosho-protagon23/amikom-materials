# Strings
first_name = "Bob"
food = "Mango"
email = "bobo.123@hubu.com"

print(f"My name is {first_name}")
print(f"I like {food}")
print(f"My address is: {email}")

# Integers
age = 25
quantity = 3
num_of_subjects = 10

print(f"I am {age} years old")
print(f"I have {quantity} dollars")
print(f"I have {num_of_subjects} subjects for class")

# Float
price = 12.50
gpa = 4.5
distance = 78.4

print(f"My watch cost me ${price}")
print(f"My gpa last year was {gpa}")
print(f"My distance was {distance} kilometers away")

# Boolean
is_stud = False
for_sale = True

if is_stud:
    print("My student is a student")
else:
    print("My student is not a student")

if for_sale:
    print("Yes still on stock")
else:
    print("No empty on stock")